namespace Godot.BindingsGeneration;

/// <summary>
/// Represents the target architecture for the generated bindings.
/// </summary>
public enum ArchBits
{
    /// <summary>
    /// Indicates that the generated bindings are targeting a 32-bit architecture.
    /// </summary>
    Bits32,

    /// <summary>
    /// Indicates that the generated bindings are targeting a 64-bit architecture.
    /// </summary>
    Bits64,
}
